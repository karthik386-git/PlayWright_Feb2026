let browser = "Chrome"
let OS = "Windows"

//AND condition &&

if(browser == "Chrome" && OS == "Windows"){
    console.log("Chrome browser is opened in Windows OS");
}
else{
    console.log("Not supported browser or OS");
}

//OR condition ||
if(browser == "Chrome" || OS == "MAC"){
    console.log("Supported browser or OS is opened");
}
else{
    console.log("Not supported browser or OS");
}
